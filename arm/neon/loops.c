#define save(dst, src) *dst = src
#define load(src) (*src)
#define LOOP1(name, rtype, itype, f, set, load, rstep, istep) \
    void name(rtype *r, itype *v, int32_t n)                  \
    {                                                         \
        while (n >= rstep)                                    \
        {                                                     \
            set(r, f(load(v)));                               \
            r += rstep;                                       \
            n -= rstep;                                       \
            v += istep;                                       \
        }                                                     \
    }

